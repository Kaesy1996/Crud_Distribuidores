    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try{
            PreparedStatement ps=cn.prepareStatement("INSERT INTO distributors(fecha, nombre_completo, direccion, tel1,tel2) VALUES (?,?,?,?,?)");
            ps.setString(1,txtFecha.getText());
            ps.setString(2,txtNombre.getText()); 
            ps.setString(3,txtDireccion.getText());
            ps.setString(4,txtTel1.getText());
            ps.setString(5,txtTel2.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"DATOS GUARDADOS CORRECTAMENTE");
            mostrardatos();
            limpiarentradas();
            
            
        }catch(SQLException e){
            System.out.println("ERROR AL REGISTRAR AL DISTRIBUIDOR" + e);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void jtabledatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtabledatosMouseClicked
        btnRegistrar.setEnabled(false);
        btnActualizar.setEnabled(true);
        int fila=this.jtabledatos.getSelectedRow();
        this.txtID.setText(this.jtabledatos.getValueAt(fila,0).toString());
        this.txtFecha.setText(this.jtabledatos.getValueAt(fila,1).toString());
        this.txtNombre.setText(this.jtabledatos.getValueAt(fila,2).toString());
        this.txtDireccion.setText(this.jtabledatos.getValueAt(fila,3).toString());
        this.txtTel1.setText(this.jtabledatos.getValueAt(fila,4).toString());
        this.txtTel2.setText(this.jtabledatos.getValueAt(fila,5).toString());
        
        
    }//GEN-LAST:event_jtabledatosMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try{
        PreparedStatement ps=cn.prepareStatement ("Update distributors set fecha = '"+txtFecha.getText()+"',nombre_completo='"+txtNombre.getText()+"',direccion='"+txtDireccion.getText()+"',tel1='"+txtTel1.getText()+"',tel2='"+txtTel2.getText()+"'where id='"+txtID.getText()+"'" );
        int indice=ps.executeUpdate();
        
        if(indice>0){
            JOptionPane.showMessageDialog(rootPane, "DATOS ACTUALIZADOS CORRECTAMENTE");
            mostrardatos();
            limpiarentradas();
        }else{
            JOptionPane.showConfirmDialog(null, "No selecciono Fila");
        }
        }catch (SQLException e){
            JOptionPane.showConfirmDialog(null, "ERROR AL ACTUALIZAR DATOS"+e);
        }
 
    }//GEN-LAST:event_btnActualizarActionPerformed

